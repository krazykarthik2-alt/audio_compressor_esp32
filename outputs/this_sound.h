// Differential DAC playback function (blocking)
void play_this_sound() {
    // Notes:
    //  - This is a blocking playback routine intended for ESP32.
    //  - It writes sample to DAC_PIN_A and inverted sample to DAC_PIN_B
    //  - Keep small series resistors (220..470 ohm) and a reconstruction low-pass
    //    (e.g. R=1k, C=33nF) in hardware to reduce HF stepping noise and damping.

    const unsigned long SAMPLE_RATE = 16000UL;
    const unsigned int SAMPLE_PERIOD_US = 1000000UL / SAMPLE_RATE; // microseconds per sample

    // Play buffer (blocking)
    for (unsigned int i = 0; i < this_sound_len; ++i) {
        uint8_t sample = pgm_read_byte_near(this_sound + i);
        // write differential outputs: A = sample, B = inverted
        dacWrite(DAC_PIN_A, sample);
        dacWrite(DAC_PIN_B, 255 - sample);
        // wait until next sample
        ets_delay_us(SAMPLE_PERIOD_US);
    }

    // restore mid-level to avoid DC bias on the piezo
    dacWrite(DAC_PIN_A, 128);
    dacWrite(DAC_PIN_B, 128);
}

